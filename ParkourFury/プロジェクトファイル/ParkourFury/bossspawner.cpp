//===========================================================================================================
// 初期化処理
//===========================================================================================================
#include "boss.h"
HRESULT CBossSpawner::Init()
{
	//プレイヤーを生成
	CBoss::Create(GetPos());

	//ギミック初期化処理
	if (FAILED(CGimmick::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

