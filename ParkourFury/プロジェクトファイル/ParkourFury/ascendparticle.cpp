{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CAscendParticle::Init()
{
	//パラメータ設定
	SetMove({ 0.0f, SPEED, 0.0f });

	//基底クラス初期化処理
	if (FAILED(CParticle::Init())) return E_FAIL;

	return S_OK;
}

//===========================================================================================================
// 終了処理
