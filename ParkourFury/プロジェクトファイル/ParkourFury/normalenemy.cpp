#include "normalenemy.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CNormalEnemy::CNormalEnemy() : CEnemy()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CNormalEnemy::~CNormalEnemy()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CNormalEnemy::Init()
{
	//基底クラス初期化処理
	if (FAILED(CEnemy::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CNormalEnemy::Uninit()
{
	//基底クラス終了処理
	CEnemy::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CNormalEnemy::Release()
{
	//基底クラス解放処理
	CEnemy::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CNormalEnemy::Update()
{
	//基底クラス更新処理
	CEnemy::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CNormalEnemy::Draw()
{
	//エディタモードの場合
	if (CManager::GetInstance()->GetScene()->GetSceneState() == CScene::SCENE::EDIT_3D)
	{//描画せずに処理を抜ける
		return;
	}

	//基底クラス描画処理
	CEnemy::Draw();
}

//===========================================================================================================
// 当たり判定
//===========================================================================================================
bool CNormalEnemy::Collision(CollisionParam& aParam)
{
	//当たり判定フラグ
	bool bCollision = false;

	//基底クラス当たり判定
	bCollision = CEnemy::Collision(aParam);

	return bCollision;
}
