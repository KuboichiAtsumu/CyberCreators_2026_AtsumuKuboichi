#include "excellent.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CExcellent::CExcellent() : CEffect2D()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CExcellent::~CExcellent()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CExcellent::Init()
{
	//生成サウンド再生
	CManager::GetInstance()->GetSound()->Play(CSound::SOUND::EXCELLENT);

	//サイズ設定
	SetSize(SIZE);

	//基底クラス初期化処理
	if (FAILED(CEffect2D::Init())) return E_FAIL;

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CExcellent::Uninit()
{
	//基底クラス終了処理
	CEffect2D::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CExcellent::Update()
{
	//基底クラス更新処理
	CEffect2D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CExcellent::Draw()
{
	//基底クラス描画処理
	CEffect2D::Draw();
}
