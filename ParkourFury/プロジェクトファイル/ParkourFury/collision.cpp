{
}

//===========================================================================================================
// 点とボックス
//===========================================================================================================
bool CCollision::PointBox3D(const D3DXVECTOR3& pos1, const D3DXVECTOR3& pos2, const D3DXVECTOR3& size2)
{
	//当たり判定フラグ
	bool bCollision = false;

	//点がボックス内にある
	if ((pos1.x >= pos2.x - size2.x * 0.5f &&
			pos1.x <= pos2.x + size2.x * 0.5f) &&
		(pos1.y >= pos2.y - size2.y * 0.5f &&
			pos1.y <= pos2.y + size2.y * 0.5f) &&
		(pos1.z >= pos2.z - size2.z * 0.5f &&
			pos1.z <= pos2.z + size2.z * 0.5f))
	{
		//フラグをtrueにする
		bCollision = true;
	}

	return bCollision;
}