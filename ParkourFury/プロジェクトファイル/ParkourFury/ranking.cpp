CRanking::CRanking() : CScene()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CRanking::~CRanking()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CRanking::Init()
{
	//背景の生成
	CBg::Create(CBg::TYPE::RANKING);

	//シーン初期化処理
	if (FAILED(CScene::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CRanking::Uninit()
{
	//シーン終了処理
	CScene::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CRanking::Update()
{
	//Enterキーが押された場合
	if (CManager::GetInstance()->GetKeyboard()->GetTrigger(DIK_RETURN)
		|| CManager::GetInstance()->GetJoypad()->GetTrigger(CInputJoypad::JOYKEY::JOYKEY_A)
		|| CManager::GetInstance()->GetJoypad()->GetTrigger(CInputJoypad::JOYKEY::JOYKEY_START))
	{
		//タイトル画面へ移行
		CManager::GetInstance()->GetFade()->SetFade(CScene::MODE::MODE_TITLE);
	}

	//基底クラス更新
	CScene::Update();
}

