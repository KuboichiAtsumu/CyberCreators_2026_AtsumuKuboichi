#include "sky.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CSky::CSky(int nPriority) : CBack3D(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CSky::~CSky()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CSky::Init()
{
	//パラメータ設定
	SetScale({ SCALE, SCALE, SCALE });//拡大率

	//Xファイルオブジェクト初期化処理
	if (FAILED(CBack3D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CSky::Uninit()
{
	//Xファイルオブジェクト終了処理
	CBack3D::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CSky::Release()
{
	//Xファイルオブジェクト解放処理
	CBack3D::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CSky::Update()
{
	//角度情報を取得
	D3DXVECTOR3 rot = GetRot();

	//回転させる
	rot.y += TURN_SPEED;

	//角度補正
	CorrectionRot(rot.y);

	//角度情報を設定
	SetRot(rot);

	//Xファイルオブジェクト更新処理
	CBack3D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CSky::Draw()
{
	//エディタモードの場合
	if (CManager::GetInstance()->GetScene()->GetSceneState() == CScene::SCENE::EDIT_3D)
	{//描画せずに処理を抜ける
		return;
	}

	//Xファイルのオブジェクト描画処理
	CBack3D::Draw();
}
