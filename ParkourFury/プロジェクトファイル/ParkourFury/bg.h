class CBg : public CObject2D
{
public:
	//======================================
	// 列挙型定義
	//======================================

	//タイプ
	enum class TYPE
	{
		NONE = 0,
		RESULT,//リザルト
		RANKING,//ランキング
		MAX
	};

	//======================================
	// 関数
	//======================================
	CBg(int nPriority = static_cast<int>(Category::BG));//コンストラクタ
	~CBg() override;//デストラクタ
	HRESULT Init() override;//初期化処理
	void Uninit() override;//終了処理
	void Update() override;//更新処理
	void Draw() override;//描画処理
	static CBg* Create(TYPE type);//生成処理

	//設定処理
	void SetBGType(TYPE type) { m_type = type; }//タイプ

private:
	//======================================
	// 変数
	//======================================
	TYPE m_type;//タイプ
};

#endif
