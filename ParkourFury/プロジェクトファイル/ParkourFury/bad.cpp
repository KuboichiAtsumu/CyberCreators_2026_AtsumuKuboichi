#include "bad.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CBad::CBad() : CEffect2D()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CBad::~CBad()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CBad::Init()
{
	//サイズ設定
	SetSize(SIZE);

	//基底クラス初期化処理
	if (FAILED(CEffect2D::Init())) return E_FAIL;

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CBad::Uninit()
{
	//基底クラス終了処理
	CEffect2D::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CBad::Update()
{
	//基底クラス更新処理
	CEffect2D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CBad::Draw()
{
	//基底クラス描画処理
	CEffect2D::Draw();
}
