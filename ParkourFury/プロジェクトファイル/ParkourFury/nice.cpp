#include "nice.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CNice::CNice() : CEffect2D()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CNice::~CNice()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CNice::Init()
{
	//生成サウンド再生
	CManager::GetInstance()->GetSound()->Play(CSound::SOUND::NICE);

	//サイズ設定
	SetSize(SIZE);

	//基底クラス初期化処理
	if (FAILED(CEffect2D::Init())) return E_FAIL;

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CNice::Uninit()
{
	//基底クラス終了処理
	CEffect2D::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CNice::Update()
{
	//基底クラス更新処理
	CEffect2D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CNice::Draw()
{
	//基底クラス描画処理
	CEffect2D::Draw();
}
