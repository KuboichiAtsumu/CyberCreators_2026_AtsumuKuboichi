#include "chasearea.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CChaseArea::CChaseArea() : CArea()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CChaseArea::~CChaseArea()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CChaseArea::Init()
{
	//基底クラス初期化処理
	if (FAILED(CArea::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CChaseArea::Uninit()
{
	//基底クラス終了処理
	CArea::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CChaseArea::Release()
{
	//基底クラス解放処理
	CArea::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CChaseArea::Update()
{
	//基底クラス更新処理
	CArea::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CChaseArea::Draw()
{
#ifdef _DEBUG
	//基底クラス描画処理
	CArea::Draw();
#endif // _DEBUG
}

//===========================================================================================================
// 当たり判定
//===========================================================================================================
bool CChaseArea::Collision(D3DXVECTOR3 pos)
{
	//当たり判定フラグ
	bool bCollision = false;

	//当たり判定がtrue
	if (CArea::Collision(pos))
	{
		//フラグをtrueにする
		bCollision = true;
	}

	return bCollision;
}
