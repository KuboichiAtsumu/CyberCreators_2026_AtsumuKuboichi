#include "great.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CGreat::CGreat() : CEffect2D()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CGreat::~CGreat()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CGreat::Init()
{
	//生成サウンド再生
	CManager::GetInstance()->GetSound()->Play(CSound::SOUND::GREAT);

	//サイズ設定
	SetSize(SIZE);

	//基底クラス初期化処理
	if (FAILED(CEffect2D::Init())) return E_FAIL;

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CGreat::Uninit()
{
	//基底クラス終了処理
	CEffect2D::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CGreat::Update()
{
	//基底クラス更新処理
	CEffect2D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CGreat::Draw()
{
	//基底クラス描画処理
	CEffect2D::Draw();
}
