#ifndef _FIELD_H_	//このマクロ定義がされなかったら
#define _FIELD_H_	//2重インクルード防止のマクロ定義

#include "main.h"

//=======================================================================================================================================================================================================================
// マクロ定義
//=======================================================================================================================================================================================================================
#define NUM_BLOCK_X		(20)									//X方向のブロック数
#define NUM_BLOCK_Z		(20)									//Z方向のブロック数
#define FIELD_VER		((NUM_BLOCK_X + 1) * (NUM_BLOCK_Z + 1))	//頂点数
#define FIELD_POLYGON	(FIELD_VER * 2 + 4 * (NUM_BLOCK_Z - 1))	//ポリゴン数
#define FIELD_INDEX		(FIELD_POLYGON + 2)						//インデックス数
#define FIELD_RADIUS	(1000)									//各円柱面の中心からの半径

//=======================================================================================================================================================================================================================
// 構造体
//=======================================================================================================================================================================================================================
typedef struct
{
	D3DXVECTOR3 pos;	//位置
	D3DXVECTOR3 rot;	//向き
	D3DXCOLOR col;		//色
	D3DXMATRIX mtxWorld;	//ワールドマトリックス
}Field;

//=======================================================================================================================================================================================================================
// プロトタイプ宣言
//=======================================================================================================================================================================================================================
void InitField(void);	//初期化処理
void UninitField(void);	//終了処理
void DrawField(void);	//描画処理
Field* GetField(void);	//情報を取得
#endif
