//=======================================================================================================================================================================================================================
// グローバル変数
//=======================================================================================================================================================================================================================
bool bMotionEdit = false;

//=======================================================================================================================================================================================================================
// 初期化処理
//=======================================================================================================================================================================================================================
void InitGame(void)
{
	InitPlayer();	//プレイヤー
	InitCamera();	//カメラ
	InitField();	//フィールド
	InitWall();		//壁
}

//=======================================================================================================================================================================================================================
// 終了処理
//=======================================================================================================================================================================================================================
void UninitGame(void)
{
	UninitPlayer();	//プレイヤー
	UninitField();	//フィールド
	UninitWall();	//壁
}

//=======================================================================================================================================================================================================================
// 更新処理
//=======================================================================================================================================================================================================================
void UpdateGame(void)
{
	UpdatePlayer();	//プレイヤー
	UpdateCamera();		//カメラ
}

//=======================================================================================================================================================================================================================
// 描画処理
//=======================================================================================================================================================================================================================
void DrawGame(void)
{
	SetCamera();	//カメラの設定
	DrawPlayer();	//プレイヤー
	DrawWall();		//壁
	DrawField();	//フィールド
}
