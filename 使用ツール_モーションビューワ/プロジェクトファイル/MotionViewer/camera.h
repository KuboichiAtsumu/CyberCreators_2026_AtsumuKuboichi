#ifndef _CAMERA_H_
#define _CAMERA_H_

#include "main.h"

//=======================================================================================================================================================================================================================
// マクロ定義
//=======================================================================================================================================================================================================================
#define GAMECAMERA_POSV_Y	(70.0f)//視点の初期Y座標
#define GAMECAMERA_POSV_Z	(-200.0f)//視点の初期Z座標
#define TITLECAMERA_POSV_Y	(450.0f)//視点のY座標
#define TITLECAMERA_POSV_Z	(3000.0f)//視点のZ座標
#define CAMERA_ROLL			(0.05f)//X軸回転速度
#define CAMERA_Y_MOVE		(0.5f)//Y軸の移動量
#define MOUSE_SINCE			(0.1f)//マウス感度

//=======================================================================================================================================================================================================================
// 構造体
//=======================================================================================================================================================================================================================
typedef struct
{
	D3DXVECTOR3 posV;//視点
	D3DXVECTOR3 posR;//注視点
	D3DXVECTOR3 vecU;//ベクトル
	D3DXVECTOR3 move;//移動量
	D3DXVECTOR3 rot;//向き
	D3DXMATRIX mtxProjection;//プロジェクションマトリックス
	D3DXMATRIX mtxView;//ビューマトリックス
	float fLength;//視点と注視点の距離
}Camera;

//=======================================================================================================================================================================================================================
// プロトタイプ宣言
//=======================================================================================================================================================================================================================
void InitCamera(void);		//初期化処理
void UpdateCamera(void);	//更新処理
void SetCamera(void);		//設定処理
Camera* GetCamera(void);	//情報の取得
#endif
